
package secao16;

public class exception {
    
    /*
    Quando falamos em exceções no Java, devemos compreender da seguinte forma:
    
    Existem dois grupos de exceções:
    
    * Checked Exceptions: (Exceções Checadas) - Menos perigosas
    - Exception
    - IOException
    - SQLException
    
    Essas exceções fazem com que o programa não seja executado
    até que a determinada exceção seja tratada.
    
    * Unchecked Exceptions: (Exceções não checadas) - Mais perigosas
    - RuntimeException
    - NullPointerException
    - ArrayOutofBoundsException
    
    Essas exceções só acontecem durante a execução do programa
    e desta forma não temos como saber, em grande parte dos casos, onde ela
    poderá ocorrer.    
    */
    
}
